var sum = function (num1, num2) {
    return num1 + num2;
}

var substraction = function (num1, num2) {
    return num1 - num2;
}

var multiplication = function (num1, num2) {
    return num1 * num2;
}

var divide = function (num1, num2) {
    return num1 / num2;
}

module.exports = {
    sum: sum,
    substraction: substraction,
    multiplication: multiplication,
    divide: divide
}